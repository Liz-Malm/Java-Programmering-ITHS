package my_programs.lesson_03;
public class Unicorn {
    int age;
    String color, mood;
    boolean horn, alive;

    Unicorn(int age, String color, String mood, boolean horn, boolean alive) {
        this.age = age;
        this.color = color;
        this.mood = mood;
        this.horn = horn;
        this.alive = alive;
    }

}